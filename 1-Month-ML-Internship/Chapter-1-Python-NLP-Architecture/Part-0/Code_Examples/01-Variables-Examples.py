# Variables and Types Examples

# Creating variables
name = "Alice"
age = 30
height = 5.7
is_student = False

# Checking types
print(type(name))      # <class 'str'>
print(type(age))       # <class 'int'>
print(type(height))    # <class 'float'>
print(type(is_student)) # <class 'bool'>

# Type conversion
age_str = "25"
age_int = int(age_str)
age_float = float(age_str)

# Common operations
x = 10
y = 3
print(x + y)  # 13
print(x - y)  # 7
print(x * y)  # 30
print(x / y)  # 3.333...
print(x // y) # 3 (floor division)
print(x ** y) # 1000 (exponent)
